    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int result=validateInputs();
        if(result==0){
            JOptionPane.showMessageDialog(null,"Password must be atleast of 4 characters long");
            return;
        }
        if(result==-1){
            JOptionPane.showMessageDialog(null,"UserId can not be left blank");
            return ;
        }
        char []pwd=txtPassword.getPassword();
        char []repwd=txtRePassword.getPassword();
        String spwd=String.valueOf(pwd);
        String srepwd=String.valueOf(repwd);
        System.out.println("Value of spwd "+ spwd);
        System.out.println("Value of rspwd "+srepwd);
        if(isPasswordMatching(spwd,srepwd)==false){
            JOptionPane.showMessageDialog(null, "Passwords do not Match");
            return;
        }
        try{
            String empid=jcEmpId.getSelectedItem().toString();
            String userid=txtUserId.getText().trim();
            String username=txtEmpName.getText();
            UserPojo u= new UserPojo();
            u.setEmpid(empid);
            u.setUserid(userid);
            u.setUsername(username);
            u.setPassword(spwd);
            u.setUsertype("Receptionist");
            boolean status=ReceptionistDao.addReceptionist(u);
            if(status){
                JOptionPane.showMessageDialog(null, "Receptionist Added SucessFully");
                clearText();
                loadReceptionistDetails();
                return;
            }
             JOptionPane.showMessageDialog(null, "Record not Added");
            
            
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Db Error!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

