    private void loadReceptionistDetails() {
         //To change body of generated methods, choose Tools | Templates.
         try{
         receptionist=ReceptionistDao.getNonRegisteredReceptionist();
         if(receptionist.isEmpty()){
             JOptionPane.showMessageDialog(null, "No un_registered receptionist present !");
             btnAdd.setEnabled(false);
             return;
         }
         btnAdd.setEnabled(true);
         Set<String> keys=receptionist.keySet();
         Iterator<String> it=keys.iterator();
            
         jcEmpId.removeAllItems();
         while(it.hasNext()){
             jcEmpId.addItem(it.next());
         }
         }catch(SQLException ex){
             JOptionPane.showMessageDialog(null, "Db Erro!");
             ex.printStackTrace();
         }
    }

